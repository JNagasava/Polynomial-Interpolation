def runge(x):
    """
    Runge function -> runge(x) = 1 / (1 + 25x²)
    """
    return 1 / (1 + 25 * x**2)

def square(x):
    """
    Square function -> square(x) = x²
    """
    return x**2

def cube(x):
    """
    Cube function -> cube(x) = x³
    """
    return x**3